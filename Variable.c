#include <stdio.h>

int main(void)
{
	int num1, num2;		//declare variable (data type: integer)
	num1=-10;		//assign value
	num2=-20;
	
	int num3=10, num4=20;		//declare variable and assign value

	printf("num1: %d, num2: %d \n", num1, num2);
	printf("num3: %d, num4: %d \n", num3, num4);
	
	return 0;
}